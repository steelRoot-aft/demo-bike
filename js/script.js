$('.icon-menu').click(function (event) {
	$(this).toggleClass('active');
	$('.menu__body').toggleClass('active');
	$('body').toggleClass('lock');
});

$('.menu__body a').click(function () {
	$('.icon-menu, .menu__body').removeClass('active');
	$('body').removeClass('lock');
});

